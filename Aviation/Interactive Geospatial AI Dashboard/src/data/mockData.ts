    'Brown Pelican': '#8B4513',
    'Royal Tern': '#4169E1',
    'Sandwich Tern': '#87CEEB',
    'Black Skimmer': '#2F4F4F',
    'Laughing Gull': '#708090',
    'Reddish Egret': '#CD5C5C',
